    - Crie uma operação com cada um dos operadores básicos;<br>
    - Cada operação deve estar em uma variável diferente;<br>
    - Imprima cada uma das etapas;<br>
    - Ex: subtração -> multplicação -> soma -> divisão.<br>

<?php

    $a = 50;
    $b = 10;

    $op1 = $a - $b;
    echo $op1 . "<br>";

    $c = 12.4;

    $op2 = $op1 * $c;
    echo $op2 . "<br>";

    $d = 4.8;

    $op3 = $op2 + $d;
    echo $op3 . "<br>";

    $e = 9.2;

    $op4 = $op3 / $e;
    echo $op4 . "<br>";

?>